print("\nDissecando uma Variável")

a = input("\nDigite algo: ") 

print ('\nO tipo primitivo deste valor é ', type(a))
print ('tem espaço: ', a.isspace())
print ('é numerico: ', a.isnumeric())
print ('Tem letra(s) ', a.isalpha())
print ('é alphanumerico: ', a.isalnum())
print ('esta e maiusculas: ', a.isupper())
print ('esta em minusculas:', a.islower())
print ('Esta capitalizada: ', a.istitle())
print()
